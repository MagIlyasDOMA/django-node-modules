from django.conf import settings

BASE_DIR = settings.BASE_DIR
NODE_MODULES_DIR = getattr(settings, 'NODE_MODULES_DIR', BASE_DIR / 'node_modules')
USE_LOCAL_NODE_MODULES_BASE_URL = getattr(settings, 'USE_LOCAL_NODE_MODULES_BASE_URL', True)
if USE_LOCAL_NODE_MODULES_BASE_URL:
    LOCAL_NODE_MODULES_BASE_URL = getattr(settings, 'LOCAL_NODE_MODULES_BASE_URL', 'node_modules')
else:
    LOCAL_NODE_MODULES_BASE_URL = ''

ALLOWED_NODE_MODULES = getattr(settings, 'ALLOWED_NODE_MODULES', '__all__')

__all__ = ['BASE_DIR', 'NODE_MODULES_DIR', 'USE_LOCAL_NODE_MODULES_BASE_URL',
           'LOCAL_NODE_MODULES_BASE_URL', 'ALLOWED_NODE_MODULES']
